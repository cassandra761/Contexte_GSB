            <h1>Notre histoire</h1>
            <p>
                GSB (Groupe Spectra Biopharma) a été fondée en 1987 par une équipe de chercheurs visionnaires
                désireux de révolutionner le secteur pharmaceutique. Depuis nos débuts modestes dans un petit laboratoire,
                nous avons grandi jusqu'à devenir l'une des entreprises pharmaceutiques les plus innovantes au monde.
            </p>
            <h2>Les débuts (1987-1995)</h2>
            <p>
                Notre histoire commence avec la création du premier centre de recherche à Lyon, en France.
                Nos équipes se sont concentrées sur le développement de molécules innovantes dans les domaines
                de l'oncologie et des maladies cardiovasculaires. Ces années pionnières ont établi les fondations
                de notre expertise scientifique et de notre engagement envers l'excellence.
            </p>
            <p>
                En 1992, nous avons lancé notre premier médicament, marquant un tournant décisif dans notre développement.
                Ce succès nous a permis d'étendre nos opérations et d'investir davantage dans la recherche et le développement.
            </p>
            <h2>L'expansion internationale (1996-2010)</h2>
            <p>
                La décennie suivante a été caractérisée par une croissance exponentielle. Nous avons ouvert des filiales
                en Europe, en Amérique du Nord et en Asie-Pacifique. Nos effectifs sont passés de 50 à 5000 collaborateurs,
                reflétant l'ampleur de notre expansion.
            </p>
            <p>
                Nous avons consolidé notre position en menant plusieurs acquisitions stratégiques de petites entreprises
                biotechnologiques prometteuses. Ces acquisitions nous ont permis d'accélérer notre portefeuille de
                développement clinique et de diversifier nos domaines thérapeutiques.
            </p>
            <h2>L'ère de la consolidation (2011-2020)</h2>
            <p>
                La fusion avec Spectra Laboratories en 2015 a marqué un moment clé dans notre histoire.
                Cette fusion a créé un géant pharmaceutique avec une présence mondiale renforcée et un portefeuille
                de produits plus robuste. Nous avons également investi massivement dans les technologies numériques
                et la bioinformatique pour rester à la pointe de l'innovation.
            </p>
            <p>
                Durant cette période, plusieurs de nos médicaments ont reçu l'approbation des autorités réglementaires
                majeures, générant des milliards de dollars de revenus et améliorant la qualité de vie de millions de patients.
            </p>
            <h2>Vers l'avenir (2021 à aujourd'hui)</h2>
            <p>
                Aujourd'hui, GSB est un leader incontesté de l'industrie pharmaceutique, avec un portefeuille diversifié
                couvrant l'oncologie, les maladies rares, les maladies infectieuses et l'immunologie. Notre équipe de
                plus de 25000 scientifiques, médecins et professionnels de la santé travaille sans relâche pour développer
                de nouveaux traitements Life-changing.
            </p>
            <p>
                Nous restons fidèles à nos valeurs fondatrices : l'intégrité, l'innovation et l'engagement envers les patients.
                Nos investissements continus dans la recherche et le développement, ainsi que notre partenariat avec les
                institutions académiques de premier plan, nous permettent de rester à la forefront de la découverte scientifique.
            </p>
            <h2>Notre engagement</h2>
            <p>
                GSB s'engage à améliorer la santé mondiale en développant des traitements innovants et en les rendant accessibles
                aux patients qui en ont besoin. Notre vision est un monde où chaque patient a accès aux meilleurs soins possibles,
                peu importe leur situation socio-économique ou leur localisation géographique.
            </p>

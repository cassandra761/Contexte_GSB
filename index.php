            <h1>Bienvenue chez GSB</h1>
            <p>
                L’industrie pharmaceutique est un secteur très lucratif dans lequel le mouvement de fusion-acquisition
                est très fort. <br>
                Les regroupements de laboratoires ces dernières années ont donné naissance à des entités gigantesques au
                sein desquelles le travail est longtemps resté organisé selon les anciennes structures. <br>
                Des déboires divers récents autour de médicaments ou molécules ayant entraîné des omplications médicales
                (ex. le Médiator),
                des voix se sont élevées pour critiquer fortement un aspect de l'activité des laboratoires,
                à savoir la visite médicale, réputée être le lieu d'arrangements entre l'industrie et les
                médecins, et tout du moins un terrain d'influence opaque. <br>
            </p>
            <h1>
                GSB, un laboratoire pharmaceutique
            </h1>
            <p>
                GSB est un laboratoire pharmaceutique français, spécialisé dans la recherche, le développement et la
                commercialisation de médicaments innovants. Fondé en 1987, GSB s'est rapidement imposé comme un acteur majeur
                de l'industrie pharmaceutique grâce à son engagement envers l'excellence scientifique et l'innovation.
                Avec une présence mondiale et un portefeuille diversifié de produits, GSB continue de repousser les limites
                de la médecine pour améliorer la santé et le bien-être des patients à travers le monde.
                Dans plus de 35 ans d'existence, GSB a développé une expertise dans plusieurs domaines thérapeutiques, notamment
                l'oncologie, les maladies cardiovasculaires, les troubles neurologiques et les maladies infectieuses. <br>
                Grâce à une stratégie de recherche et développement axée sur l'innovation, GSB a lancé de nombreux médicaments révolutionnaires qui ont transformé la vie de millions de patients à travers le monde. <br>
                En plus de son engagement envers la recherche, GSB est également reconnu pour son approche éthique et responsable dans toutes ses activités. L'entreprise s'efforce de maintenir les normes les plus élevées en matière de sécurité, d'efficacité et de qualité dans tous ses produits, tout en respectant les réglementations strictes de l'industrie pharmaceutique. <br>
                Avec une équipe de chercheurs, de scientifiques et de professionnels de la santé dévoués, GSB continue de faire progresser la science médicale et de contribuer à l'amélioration de la santé mondiale. <br>
            </p>
